function mediaFunction(){
    document.getElementsByClassName("afterMedia")[0].style.display="block";
    document.getElementsByClassName("hamburger")[0].style.display="none";
    document.getElementsByClassName("x")[0].style.display="block";
}

function RmediaFunction(){
    document.getElementsByClassName("afterMedia")[0].style.display="none";
    document.getElementsByClassName("hamburger")[0].style.display="block";
    document.getElementsByClassName("x")[0].style.display="none";
}






